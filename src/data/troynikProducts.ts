export function getTroynikSizes(materialName: string, colorCode: string, connectionType: ConnectionType = "rastrub"): TroynikSize[] {
  const mat = materials.find((m) => m.name === materialName);
  if (!mat) return [];
  const specs = materialSpecs[materialName];
  const hasMultipleColors = specs && specs.colors.length > 1;
  const prefix = connectionType === "flanec" ? "ТРФ" : "ТР";
  const sizes = connectionType === "flanec" ? baseTroynikFlanecSizes : baseTroynikSizes;
  return sizes.map((item) => ({
    ...item,
    article: hasMultipleColors
      ? `СЗПК.${prefix}.${mat.code}.${colorCode}.${item.d}x${item.d1}`
      : `СЗПК.${prefix}.${mat.code}.${item.d}x${item.d1}`,
  }));
}

